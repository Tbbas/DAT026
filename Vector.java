public class Vector {
    public double speedX;
    public double speedY;
    public double x;
    public double y;
    public Vector(double speedX, double speedY, double x, double y){
        this.speedX = speedX;
        this.speedY = speedY;
        this.x = x;
        this.y = y;
    }
}
